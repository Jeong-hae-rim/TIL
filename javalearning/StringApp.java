package javalearning;

public class StringApp {

	public static void main(String[] args) {
		
		System.out.println("Hello World"); //String. 캐릭터가 모여있는 타입.
		System.out.println('H');  
		//작은 따옴표는 특수한 데이터 타입. Character - 한 글자를 가리키는 데이터 타입.
		System.out.println("H"); //문자와 문자열은 다름.
		
		System.out.println("Hello "
				+ "World"); //걍 더해줬지 줄바꿈 아님
		System.out.println("Hello \nWorld"); // \n - new line의 약자
		System.out.println("Hello \"World\""); // Hello "World" ???
		//escape한다? 역슬래시를 통해서 임무가 있는 문자의 임무를 일시적으로 도망, 해방시키는 것.
	

	}

}
